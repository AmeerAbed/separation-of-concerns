// there are 5 basic primitive types in JavaScript
// the typeof operator will tell you what type something is


console.assert(
  (typeof true) === 'boolean',
  'booleans are type "boolean"'
);

console.assert(
  (typeof "in quotes") === 'string',
  'strings are type "string"'
);

console.assert(
  (typeof 1) === 'number',
  'numbers are type "number"'
);

console.assert(
  (typeof undefined) === 'undefined',
  'undefined is type "undefined"'
);

console.assert(
  (typeof null) === 'object',
  'null is type ... "object"?'
);
